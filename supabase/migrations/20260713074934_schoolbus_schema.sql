create extension if not exists btree_gist with schema extensions;

create table public.school_settings (
  id smallint primary key check (id = 1),
  school_year integer not null,
  start_date date not null,
  end_date date not null,
  include_labor_day smallint not null default 1 check (include_labor_day in (0, 1)),
  include_election_day smallint not null default 1 check (include_election_day in (0, 1)),
  updated_at timestamptz not null default now()
);

create table public.buses (
  id bigint generated by default as identity primary key,
  bus_number integer not null unique check (bus_number > 0),
  plate_number text,
  driver_name text,
  attendant_name text,
  active smallint not null default 1 check (active in (0, 1))
);

create table public.students (
  id bigint generated by default as identity primary key,
  name text not null,
  grade integer not null check (grade between 1 and 6),
  class_name text not null,
  active smallint not null default 1 check (active in (0, 1))
);

create table public.assignments (
  id bigint generated by default as identity primary key,
  student_id bigint not null references public.students(id) on delete cascade,
  bus_id bigint not null references public.buses(id),
  stop_name text,
  start_date date not null,
  end_date date not null,
  check (start_date <= end_date),
  exclude using gist (
    student_id with =,
    daterange(start_date, end_date, '[]') with &&
  )
);

create table public.calendar_exclusions (
  id bigint generated by default as identity primary key,
  date date not null unique,
  kind text not null check (kind in ('discretionary_holiday', 'emergency', 'other')),
  note text
);

create table public.daily_runs (
  id bigint generated by default as identity primary key,
  bus_id bigint not null references public.buses(id),
  date date not null,
  status text not null check (status in ('operated', 'not_operated')),
  reason text,
  note text,
  unique (bus_id, date)
);

create table public.boarding_records (
  id bigint generated by default as identity primary key,
  daily_run_id bigint not null references public.daily_runs(id) on delete cascade,
  student_id bigint not null references public.students(id),
  boarded smallint not null default 0 check (boarded in (0, 1)),
  note text,
  unique (daily_run_id, student_id)
);

create table public.app_users (
  id bigint generated by default as identity primary key,
  username text not null unique,
  display_name text,
  role text not null check (role in ('admin', 'driver', 'attendant')),
  pin_salt text not null,
  pin_hash text not null,
  active smallint not null default 1 check (active in (0, 1))
);

create table public.sessions (
  id text primary key,
  user_id bigint not null references public.app_users(id) on delete cascade,
  expires_at timestamptz not null,
  created_at timestamptz not null default now()
);

create table public.login_throttles (
  username text primary key,
  attempt_count integer not null default 0,
  window_started_at timestamptz not null
);

create table public.inspection_groups (
  id bigint generated by default as identity primary key,
  name text not null,
  active smallint not null default 1 check (active in (0, 1))
);

create table public.inspection_group_buses (
  id bigint generated by default as identity primary key,
  group_id bigint not null references public.inspection_groups(id) on delete cascade,
  bus_id bigint not null references public.buses(id) on delete cascade,
  unique (group_id, bus_id),
  unique (bus_id)
);

create table public.checklist_items (
  id bigint generated by default as identity primary key,
  code text not null unique,
  category text not null,
  content text not null,
  responsible_role text not null default 'all' check (responsible_role in ('all', 'driver', 'attendant')),
  sort_order integer not null,
  active smallint not null default 1 check (active in (0, 1))
);

create table public.user_bus_assignments (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.app_users(id) on delete cascade,
  bus_id bigint not null references public.buses(id) on delete cascade,
  start_date date not null,
  end_date date not null,
  check (start_date <= end_date)
);

create table public.monthly_inspections (
  id bigint generated by default as identity primary key,
  month text not null check (month ~ '^20[0-9]{2}-(0[1-9]|1[0-2])$'),
  group_id bigint not null references public.inspection_groups(id),
  status text not null default 'draft' check (status in ('draft', 'complete', 'submitted')),
  submitted_at timestamptz,
  unique (month, group_id)
);

create table public.monthly_inspection_buses (
  id bigint generated by default as identity primary key,
  inspection_id bigint not null references public.monthly_inspections(id) on delete cascade,
  bus_id bigint not null references public.buses(id),
  unique (inspection_id, bus_id)
);

create table public.inspection_responses (
  id bigint generated by default as identity primary key,
  inspection_id bigint not null references public.monthly_inspections(id) on delete cascade,
  item_code text not null references public.checklist_items(code),
  answer text not null check (answer in ('yes', 'no', 'not_applicable')),
  note text,
  unique (inspection_id, item_code)
);

create index assignments_bus_dates_idx on public.assignments (bus_id, start_date, end_date);
create index daily_runs_date_status_idx on public.daily_runs (date, status);
create index user_bus_assignments_lookup_idx on public.user_bus_assignments (user_id, bus_id, start_date, end_date);
create index inspection_group_buses_group_idx on public.inspection_group_buses (group_id);
create index sessions_expiry_idx on public.sessions (expires_at);

