do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'school_settings', 'buses', 'students', 'assignments', 'calendar_exclusions',
    'daily_runs', 'boarding_records', 'app_users', 'sessions', 'login_throttles',
    'inspection_groups', 'inspection_group_buses', 'checklist_items',
    'user_bus_assignments', 'monthly_inspections', 'monthly_inspection_buses',
    'inspection_responses'
  ]
  loop
    execute format('alter table public.%I enable row level security', table_name);
    execute format('revoke all on table public.%I from anon, authenticated', table_name);
    execute format('grant select, insert, update, delete on table public.%I to service_role', table_name);
  end loop;
end
$$;

